def compose_params(id: int) -> str:
    params = db.get_user_params(id)

    text = f"""–≥–æ—Ä–æ–¥: <b>{params['town']}</b>
—Ü–µ–Ω–∞: <b>{params['minCost']}$ - {params['maxCost']}$</b>
–æ—Ç: <b>{params['landlord']}</b>
–∫–æ–º–Ω–∞—Ç: <b>{', '.join(params['rooms'])}</b>\n"""

    if params['isSubwayNeed']:
        text = text + "–º–µ—Ç—Ä–æ:"
        for st in params['subways']:
            text = text + '\n<b>' + subways[int(st)].name + '</b>'
        text = text + f"\n–¥–æ –±–ª–∏–∂–∞–π—à–µ–≥–æ: <b>{params['subway_dist']}</b> –º"
    else:
        text = text + '–º–µ—Ç—Ä–æ: <b>–ù–µ –≤–∞–∂–Ω–æ</b>'

    if params['isPointNeed']:
        text = text + f'\n—à–∏—Ä–æ—Ç–∞: <b>{params["lat"]}</b>\n–¥–æ–ª–≥–æ—Ç–∞: <b>{params["lon"]}</b>\n–≤ —Ä–∞–¥–∏—É—Å–µ: <b>{params["point_dist"]}</b> –º'
    else:
        text = text + '\n–ø–æ —Ç–æ—á–∫–µ –Ω–∞ –∫–∞—Ä—Ç–µ <b>–Ω–µ –≤—ã–±—Ä–∞–Ω–æ</b>'

    return text


